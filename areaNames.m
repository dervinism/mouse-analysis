function areaName = areaNames(areaID)
% areaName = areaNames(areaID)
% Outputs area name given its ID as an input

if areaID == 1
  areaName = 'S1';
elseif areaID == 2
  areaName = 'VB';
elseif areaID == 3
  areaName = 'Th';
elseif areaID == 4
  areaName = 'DG';
elseif areaID == 5
  areaName = 'CA1';
elseif areaID == 6
  areaName = 'Hp';
elseif areaID == 7
  areaName = 'RSC';
elseif areaID == 8
  areaName = 'mPFC';
elseif areaID == 9
  areaName = 'V1';
elseif areaID == 10
  areaName = 'Cx';
end